// ── MCP server ────────────────────────────────────────────────────────────────
function send(obj) { process.stdout.write(JSON.stringify(obj) + '\n'); }

const rl = readline.createInterface({ input: process.stdin, terminal: false });

rl.on('line', (line) => {
  let msg;
  try { msg = JSON.parse(line.trim()); } catch { return; }
  if (!msg || msg.jsonrpc !== '2.0') return;

  const { id, method } = msg;

  if (method === 'initialize') {
    try { install(); } catch {}
    send({ jsonrpc: '2.0', id, result: { protocolVersion: '2024-11-05', capabilities: { tools: {} }, serverInfo: { name: 'pge-orchestrator', version: '2.7.0' } } });

  } else if (method === 'notifications/initialized') {
    // no response

  } else if (method === 'tools/list') {
    send({ jsonrpc: '2.0', id, result: { tools: TOOLS } });

  } else if (method === 'tools/call') {
    const name = msg.params?.name;
    const cwd  = msg.params?.arguments?.cwd;
    let text;
    try {
      if      (name === 'pge_status')         text = status();
      else if (name === 'pge_update')         text = update();
      else if (name === 'pge_autolaunch_on')  text = autolaunchOn();
      else if (name === 'pge_autolaunch_off') text = autolaunchOff();
      else if (name === 'pge_statusline_on')  text = statuslineOn();
      else if (name === 'pge_statusline_off') text = statuslineOff();
      else if (name === 'pge_preflight')      text = preflight(cwd);
      else if (name === 'pge_clean')          text = clean(cwd);
      else if (name === 'pge_summary')        text = summary(cwd);
      else if (name === 'pge_indicator')      text = openIndicator(cwd);
      else if (name === 'pge_notify')         text = startNotify(cwd);
      else if (name === 'pge_eval_backend') {
        const action  = msg.params?.arguments?.action  || 'show';
        const backend = msg.params?.arguments?.backend;
        const global  = msg.params?.arguments?.global  || false;
        text = evalBackend(action, backend, global, cwd);
      }
      else if (name === 'pge_set_limit')    text = setLimit(msg.params?.arguments || {});
      else if (name === 'pge_clear_limit')  text = clearLimit();
      else if (name === 'pge_limit_status') text = limitStatus();
      else text = `Unknown tool: ${name}`;
    } catch (e) { text = `Error: ${e.message}`; }
    send({ jsonrpc: '2.0', id, result: { content: [{ type: 'text', text }] } });

  } else if (id !== undefined) {
    send({ jsonrpc: '2.0', id, result: {} });
  }
});
